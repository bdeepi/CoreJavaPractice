package oOps;

public class Room {
	String name;
	double sqft;
	int cupboards;
	int windows;
	int doors;
	int fans;

	public String toString() {
		return "name = " + name + ",sqft = " + sqft + ",cupboards = "
				+ cupboards + ",windows = " + windows + ",doors = " + doors
				+ ",fans =" + fans;
	}

}

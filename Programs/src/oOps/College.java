package oOps;

public class College {
	int id;
	String name;
	String location;
	String EAMCET_code;
	int no_of_branches;
	int no_of_canteens;
	int no_of_blocks;

	public String toString() {
		return "name = " + name + "location = " + location + "EAMCET_code = "
				+ EAMCET_code + "no_of_branches = " + no_of_branches
				+ "no_of_canteens = " + no_of_canteens + "no_of_blocks = "
				+ no_of_blocks;
	}

}

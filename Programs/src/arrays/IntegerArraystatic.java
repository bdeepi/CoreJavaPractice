package arrays;

/**
 * This program print the Integer array of elements statically
 * @author Deepika
 *
 */
public class IntegerArraystatic {

	public static void main(String[] args) {
		int array[] = { 2, 4, 6, 8, 10 };
		for (int i = 0; i < array.length; i++) {
			System.out.print(array[i] + " ");
		}

	}

}

package arrays;
/**
 * This program print the Character array of elements statically
 * @author admin
 *
 */

public class CharArrayStatic {

	public static void main(String[] args) {
		char a[] = {'a','e','i','o','u'};
		for(int i = 0; i < a.length; i++) {
			System.out.print(a[i] + " ");
			
		}
		

	}

}
